package Iuliia;

public class PosNegZero {
    public static void main(String[] args) {

        int number = 20;
        if(number > 0){
            System.out.println("Positive");
        }
        if(number < 0){
            System.out.println("Negative");
        }
        if(number == 0){
            System.out.println("Zero");
        }

    }
}
