public class Swap {
    public static void main(String[] args) {


        int x = 10,
                y = 15;

        x = x + y; //25
        y = x - y; //25-15=10
        x = x - y; //25-10=15
        System.out.println("x=" + y + "\ny=" + x);


    }
}
